


// 3 - Conversor de Turno: Receba uma letra ("M", "V" ou "N") e imprima "Bom Dia!", "Boa Tarde!" ou "Boa Noite!". Exiba "Inválido" para outras entradas. 
 

export function if_3(turno) {
  


if (turno == 'm') {
  return "bom dia"
}

if (turno == 't') {
  return "boa tarde"                 
}

if (turno == 'n') {
  return "boa noite"
}
else{
  return "inválido"
}

}


